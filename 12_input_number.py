#Belajar input pakai number/integer
print("Lagi belajar input number")
print("Masukkan angka ke-1")
a = int (input())
print("Masukkan angka ke-2")
b = int(input())

hasil_tambah = a + b
hasil_kurang = a - b
hasil_kali = a * b
hasil_bagi = a / b
hasil_kuadrat = a ** 2
hasil_akar_kuadrat = a // b

print(f"hasil dari {a} + {b} = {hasil_tambah}" )
print(f"hasil dari {a} - {b} = {hasil_kurang}" )
print(f"hasil dari {a} X {b} = {hasil_kali}" )
print(f"hasil dari {a} / {b} = {hasil_bagi}" )
print(f"hasil dari {a} kuadrat {b} = {hasil_kuadrat}" )
print(f"hasil dari {a} akar kuadrat {b} = {hasil_akar_kuadrat}" )